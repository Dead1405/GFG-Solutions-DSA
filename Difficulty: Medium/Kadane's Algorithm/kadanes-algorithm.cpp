class Solution {
  public:
    // Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(vector<int> &arr) {
        //sliding window approach 
        long long sum = arr[0],ans = arr[0];
        for(int i = 1;i<arr.size();i++){
            if(sum<0){
                sum = arr[i];
            }
            else{
                sum += arr[i];
            }
            if(sum>ans) ans = sum;
        }
        return ans;
    }
};

